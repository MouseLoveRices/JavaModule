package dichvu;

public class TheHinh {
    private float phiCoBan;
    public  String chiPhiLopHoc;


    public TheHinh(float phiCoBan, String chiPhiLopHoc) {
        this.phiCoBan = phiCoBan;
        this.chiPhiLopHoc = chiPhiLopHoc;
    }


    public float getPhiCoBan() {
        return phiCoBan;
    }


    public void setPhiCoBan(float phiCoBan) {
        this.phiCoBan = phiCoBan;
    }


    public String getChiPhiLopHoc() {
        return chiPhiLopHoc;
    }


    public void setChiPhiLopHoc(String chiPhiLopHoc) {
        this.chiPhiLopHoc = chiPhiLopHoc;
    }
    
    
    
}
